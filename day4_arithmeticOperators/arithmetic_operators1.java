package day4_arithmeticOperators;

public class arithmetic_operators1 {

	public static void main(String[] args) {		
		
		//addition
		
		System.out.println(55+5);
		int chairs=140;
		System.out.println(chairs+2);
		int moreChairs=chairs+5;
		System.out.println(moreChairs);
		
		//subtraction
		
        int line_of_code=50;
        line_of_code=50-2;
        System.out.println("line of code is "+ line_of_code);
        
        //multiplication *
        
        int classes=5;
        System.out.println(22*2);
        
        int total_line_of_code=line_of_code*classes;
        System.out.println("total line of code = "+ total_line_of_code);
        
        //division /
        
        System.out.println(30/3);
        
        int sinif=60;
        int kisi=20;
        int kalem=sinif/kisi;
        System.out.println(kalem);
        //System.out.println(11/0);error
        
        System.out.println(10/3);
        //bolumu soyluyor kalani bosveriyor
        
        //reminder %  mod a gore kalan
        
        System.out.println(10%2);
        System.out.println(15%4);
        
        		
		
		
		

	}

}
